    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:

        try{
            name1=username.getText().trim();
            pass1=password.getText().trim();

            String name2="",mob="",add="",gen="",pro="",doc="",pass="";

            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql:///shopping_management_system","root","");
            PreparedStatement p=con.prepareStatement("select * from shopping_table where `name`='"+name1+"' && `password`='"+pass1+"'");

            ResultSet rs=p.executeQuery();
            while(rs.next())
            {
                name2=rs.getString("name");
                mob=rs.getString("mobile_no");
                add =rs.getString("adress");
                gen=rs.getString("gender");
                pro=rs.getString("proffession");
                doc=rs.getString("documents");
                pass=rs.getString("password");

            }
            if(add.equals("") && gen.equals(""))
            {
                JOptionPane.showMessageDialog(User_Login.this,"login unsuccessful");
                username.setText(" ");
                password.setText("");

            }
            else{
                JOptionPane.showMessageDialog(User_Login.this,"login successful");
                username.setText(" ");
                password.setText("");

                View_Customer_Profile_User s=new View_Customer_Profile_User(name2,mob,add,gen,pro,doc,pass);
                s.setVisible(true);

            }

            p.close();
            con.close();
        }
        catch(Exception e1)
        {
            System.out.println(e1);
        }
    }//GEN-LAST:event_loginActionPerformed

