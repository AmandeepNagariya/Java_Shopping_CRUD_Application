public ArrayList<Display> displaylist()
    {
        ArrayList<Display>displaylist=new ArrayList();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql:///shopping_management_system","root","");
            String query1="select * from shopping_table";
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(query1);
            Display display;
            while(rs.next())
            {
                display=new Display(rs.getInt("id"),rs.getString("name"),rs.getString("mobile_no"),rs.getString("adress"),rs.getString("gender"),rs.getString("proffession"),rs.getString("documents"),rs.getString("password"));
                displaylist.add(display);
            }
    }
   catch(Exception e)
        {
            System.out.println(e.getMessage());
        }         
         return displaylist;
    }
    public void show_user()
    {
        ArrayList<Display> list=displaylist();
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        Object[] row=new Object[8];
        for(int i=0;i<list.size();i++)
        {
            row[0]=list.get(i).getid();
            row[1]=list.get(i).getname();
            row[2]=list.get(i).getmobile_no();
            row[3]=list.get(i).getadress();
            row[4]=list.get(i).getgender();
            row[5]=list.get(i).getproffession();
            row[6]=list.get(i).getdocuments();
            row[7]=list.get(i).getpassword();
            model.addRow(row);
        }
    }
